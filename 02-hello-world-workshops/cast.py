# Cast means convert to a diffrent type

a = 0
b = -1.5

# String
a_str = str(a)
b_str = str(b)
print(a, type(a), "[casted to string]", a_str, type(a_str))
print(b, type(b), "[casted to sting]", b_str, type(b_str))
print()

# Float
a_float = float(a)
b_float = float(b)
print(a, type(a), "[casted to float]", a_float, type(a_float))
print(b, type(b), "[casted to float]", b_float, type(b_float))
print()

# Integer
a_int = int(a)
b_int = int(b)
print(a, type(a), "[casted to int]", a_int, type(a_int))
print(b, type(b), "[casted to int]", b_int, type(b_int))
print()

# Boolean
a_bool = bool(a)
b_bool = bool(b)
print(a, type(a), "[casted to bool]", a_bool, type(a_bool))
print(b, type(b), "[casted to bool]", b_bool, type(b_bool))
print()